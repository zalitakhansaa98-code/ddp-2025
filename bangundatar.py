def persegi(sisi):
    return sisi * sisi

def persegi_panjang(panjang, lebar):
    return panjang * lebar
    
def segitiga(alas, tinggi):
    return 0.5* alas * tinggi

def lingkaran(jari_jari):
    return 3.14 * jari_jari * jari_jari
    
    